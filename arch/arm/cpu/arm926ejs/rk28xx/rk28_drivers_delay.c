#define     IN_DELAY
#include  <asm/arch/rk28_drivers.h>
#define     DELAY_ARM_FREQ      50

/***************************************************************************
函数描述:延时
入口参数:cycle数
出口参数:
调用函数:
***************************************************************************/
void DRVDelayCyc(uint32 count)
{
    count/=4;           //因为每条while循环需要4个CYC, 假设MEM是0等待
    while (count--)
        ;
}

/***************************************************************************
函数描述:延时
入口参数:us数
出口参数:
调用函数:
***************************************************************************/
void DRVDelayUs(uint32 count)
{
    while (count--)
        DRVDelayCyc(DELAY_ARM_FREQ);
}

/***************************************************************************
函数描述:延时
入口参数:ms数
出口参数:
调用函数:
***************************************************************************/
void DRVDelayMs(uint32 count)
{
    while (count--)
        DRVDelayUs(1000);
}

/***************************************************************************
函数描述:延时
入口参数:s数
出口参数:
调用函数:
***************************************************************************/
void DRVDelayS(uint32 count)
{
    while (count--)
        DRVDelayMs(1000);
}

